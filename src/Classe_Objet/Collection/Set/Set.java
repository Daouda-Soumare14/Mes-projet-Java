package Classe_Objet.Collection.Set;

public class Set
{
    int valeur1;
    int valeur2;

    public Set(int valeur1, int valeur2)
    {
        this.valeur1 = valeur1;
        this.valeur2 = valeur2;
    }

    public int getValeur2() {
        return valeur2;
    }

    public int getValeur1() {
        return valeur1;
    }
}
