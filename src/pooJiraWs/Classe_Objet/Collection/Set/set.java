package pooJiraWs.Classe_Objet.Collection.Set;

public class set {
    int valeur1;
    int valeur2;

    public set(int valeur1, int valeur2) {
        this.valeur1 = valeur1;
        this.valeur2 = valeur2;
    }

    public int getValeur2() {
        return this.valeur2;
    }

    public int getValeur1() {
        return this.valeur1;
    }
}